package Main;

public class Calculator {
    public Calculator(){}
    
    public int add(int numA, int numB){
        return numA+numB;
    }

    public int subtract(int numA, int numB){
        return numA-numB;
    }

    public int multiply(int numA, int numB){
        return numA*numB;
    }

    public int divide(int numA, int numB){
        return numA/numB;
    }
}
